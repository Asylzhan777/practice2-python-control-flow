# Пример 1: Цикл while
i = 1
while i <= 5:
    print(i)
    i += 1

i = 1
while i <= 7:
    print(i)
    i += 1

fruits = ["apple", "banana", "orange"]
i = 0

while i < len(fruits):
    print(fruits[i])
    i += 1

i = 1
total = 0

while i <= 5:
    total += i
    i += 1

print(total)

i = 5
while i > 0:
    print(i)
    i -= 1


